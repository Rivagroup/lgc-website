    <h4>Allgemeine Geschäftsbedingungen</h4>
</header>
<p>
    Alle Leistungen, welche die Rivagroup GmbH für Sie erbringt, erfolgen ausschließlich auf der
    Grundlage der nachstehenden allgemeinen Geschäftsbedingungen (AGB). Von unseren AGB abweichende
    Regelungen
    gelten nur dann, wenn diese von der Geschäftsleitung schriftlich bestätigt wurden.
</p>
<ol>
    <li>
        Der Anlaufbereich der Bowlingbahnen darf nur mit Bowlingschuhen betreten werden. Turnschuhe oder
        anderes Schuhwerk ist nicht gestattet. Bowlingschuhe können am Counter gegen eine Mietgebühr
        ausgeliehen werden. Das Verlassen des Bowlingcenters mit Bowlingschuhen ist strengstens
        untersagt.
    </li>
    <li>
        Das Betreten der Bahnen und der Servicegänge ist strengstens untersagt. Die Bahnen sind geölt,
        so dass beim Übertreten der Foullinie Rutschgefahr besteht. Für entstandene Schäden bzw.
        Verletzungen übernehmen wir keine Haftung.
    </li>
    <li>
        Für Schäden oder Verlust von privaten Bowlingschuhen, Bowlingbällen, Garderobe oder privaten
        Eigentum wird keine Haftung übernommen.
    </li>
    <li>
        Pro Bahn können maximal 8 Personen spielen.
    </li>
    <li>
        Die Bahnen werden am Counter vergeben. Ein Anspruch auf bestimmte Bahnen besteht nicht.
    </li>
    <li>
        Bowlingspieler haben sich 10 Minuten vor ihrer Reservierungszeit am Counter einzufinden.
        Die Rivagroup GmbH ist berechtig Kundendaten zu Reservierungszwecken zu speichern, zu
        verarbeiten und auszuwerten. Mit Äußerung des Reservierungswunsches wird der Nutzung der Daten
        zugestimmt.
    </li>
    <li>
        Bei Unpünktlichkeit gilt die Reservierung 10 Minuten nach Reservierungsbeginn als storniert. Die
        Bahnen können dann weiter vermietet werden.
    </li>
    <li>
        Alle Bahnen-, Veranstaltungs- und Speisereservierungen sind verbindlich und werden mit Namen,
        Anschrift sowie Telefonnummer angenommen. Das Counter- bzw. Servicepersonal ist berechtigt, die
        Richtigkeit der Angaben zu überprüfen.
    </li>
    <li>
        Bei Nichtinanspruchnahme der Reservierung bzw. Bestellung gilt folgendes: Erfolgt keine
        Stornierung
        der Reservierung / Bestellung spätestens am Vortag des Buchungstermins, wird entsprechend der
        jeweils gültigen Preisliste der geltende Betrag in Rechnung gestellt.
    </li>
    <li>
        Bei Gruppenreservierungen/Events ab 15 Personen gelten folgende Stornobedingungen:
        <ul class="list-group">
            <li class="list-group-item">bei Stornierung mehr als 2 Wochen vor dem Event: 0%,</li>
            <li class="list-group-item">bei Stornierung innerhalb von 1 bis 2 Wochen vor dem Event: 30%,
            </li>
            <li class="list-group-item">bei Stornierung innerhalb von 1 Woche bis 3 Tage vor dem Event:
                50%,</li>
            <li class="list-group-item">bei Stornierung innerhalb von 72 Stunden oder weniger vor dem
                Event: 90%.</li>
        </ul>
        Maßgeblicher Zeitpunkt ist der Zugang der Stornoerklärung bei der Rivagroup GmbH. Dem Kunden
        bleibt es unbenommen, nachzuweisen, dass der Rivagroup GmbH kein oder ein geringerer als der
        vorgenannte Schaden entstanden ist. Änderungen der Personenzahl sind der Rivagroup GmbH
        schriftlich oder per Mail bis spätestens eine Woche vor Veranstaltungsbeginn mitzuteilen.
        Erfolgt die Änderung nicht fristgerecht, wird der Pauschalpreis auf Basis der gebuchten
        Personenzahl berechnet.
    </li>
    <li>
        Bahndefekte, die nicht sofort behoben werden können, sowie technische Ausfälle durch „höhere
        Gewalt“ (Stromausfälle o.ä.) berechtigen nicht zur Forderung von Ersatzansprüchen.
    </li>
    <li>
        Sollte ein pünktlicher Spielbeginn durch Verzögerungen im Laufe des Betriebes (z.B. Bahndefekte)
        nicht
        möglich sein, besteht keine Berechtigung zur Forderung von Ersatzansprüchen. Die Bowling Plus
        GmbH
        ist
        nicht verpflichtet, über einen unpünktlichen Spielbeginn, der geringer als 1 Stunde ist, vorab
        in
        jeglicher Weise in Kenntnis zu setzen.
    </li>
    <li>
        Mutwillige Beschädigung der Einrichtung, Diebstahl, Zechprellerei, Drogenmissbrauch o.ä. werden
        generell zur Anzeige gebracht.
    </li>
    <li>
        Das Counter- bzw. Servicepersonal ist berechtigt die Bahnmiete vor Beginn, sowie Speisen und
        Getränke sofort nach dem Servieren, zu kassieren.
    </li>
    <li>
        Der Verzehr von mitgebrachten Speisen und Getränken ist im gesamten Center untersagt. Das
        Mitbringen von Kuchen und Torten ist ebenfalls nicht gestattet.
    </li>
    <li>
        Speisen und Getränke sind im Anlaufbereich nicht gestattet. Verunreinigungen sind sofort dem
        Servicepersonal zu melden.
    </li>
    <li>
        Das Werfen von Bällen bevor die Bahn freigegeben ist, ist untersagt. Für Schäden, die durch
        Missachtung entstehen, haftet der Spieler.
    </li>
    <li>
        Kinder dürfen sich im gesamten Bowlingcenter nicht unbeaufsichtigt aufhalten. Der Betreiber
        übernimmt keinerlei Haftung für Verletzungen oder Schäden die sich durch eine unterlassene
        Aufsichtspflicht seitens der Eltern oder etwaiger Aufsichtspersonen ergeben.
    </li>
    <li>
        Den Anweisungen des Service-, bzw. Aufsichtspersonals ist Folge zu leisten. Verstöße führen zur
        Sperrung der Bahnen und zur Verweisung des Hauses.
    </li>
    <li>
        Die Einhaltung des Jugendschutzgesetzes wird kontrolliert.
        Mit der verbindlichen Reservierung oder Inanspruchnahme einer Leistung erkennen unsere Gäste die
